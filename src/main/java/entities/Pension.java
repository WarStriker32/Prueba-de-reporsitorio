package entities;

public class Pension {
	private int cod_pension;
	private String name_pension;
	
	
	public int getCod_pension() {
		return cod_pension;
	}
	public void setCod_pension(int cod_pension) {
		this.cod_pension = cod_pension;
	}
	public String getName_pension() {
		return name_pension;
	}
	public void setName_pension(String name_pension) {
		this.name_pension = name_pension;
	}
}
